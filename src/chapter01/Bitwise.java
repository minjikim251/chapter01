package chapter01;

public class Bitwise {

	public static void main(String[] args) {
		int a = 4;
		int b = 2;
		
		System.out.println(a & b);
		System.out.println(a | b);
		System.out.println(a ^ b);
		System.out.println(~a);
		
		System.out.println(a>>3);
		System.out.println(a<<3);
		

	}

}
